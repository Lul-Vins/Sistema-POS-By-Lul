class Command(BaseCommand):
    help = 'Pobla el inventario con ~500 productos de prueba variados'

    def add_arguments(self, parser):
        parser.add_argument(
            '--limpiar',
            action='store_true',
            help='Eliminar todos los productos y categorías antes de poblar',
        )

    def handle(self, *args, **options):
        if options['limpiar']:
            Producto.objects.all().delete()
            Categoria.objects.all().delete()
            self.stdout.write(self.style.WARNING('  Inventario limpiado.'))

        total_creados = 0
        total_omitidos = 0

        for nombre_cat, productos in DATOS.items():
            cat, _ = Categoria.objects.get_or_create(nombre=nombre_cat)

            for (nombre, codigo, precio, costo, stock, stock_min,
                 por_peso, iva) in productos:

                if Producto.objects.filter(codigo_barras=codigo).exists():
                    total_omitidos += 1
                    continue

                Producto.objects.create(
                    categoria=cat,
                    nombre=nombre,
                    codigo_barras=codigo,
                    precio_usd=Decimal(str(precio)),
                    costo_usd=Decimal(str(costo)),
                    stock_actual=Decimal(str(stock)),
                    stock_minimo=Decimal(str(stock_min)),
                    vendido_por_peso=por_peso,
                    alicuota_iva=iva,
                    activo=True,
                )
                total_creados += 1

        self.stdout.write(self.style.SUCCESS(
            f'  Productos creados : {total_creados}'
        ))
        if total_omitidos:
            self.stdout.write(self.style.WARNING(
                f'  Omitidos (ya exist.): {total_omitidos}'
            ))
        self.stdout.write(self.style.SUCCESS('  Listo.'))
